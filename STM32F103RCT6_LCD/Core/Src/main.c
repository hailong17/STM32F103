  /*Configure GPIO pin Output Level */
  HAL_GPIO_WritePin(GPIOB, RS_PIN|EN_PIN, GPIO_PIN_RESET);

  /*Configure GPIO pins : D4_Pin D5_Pin D6_Pin D7_Pin */
